package Gun08;

public class _04_JavaAssignmentOperators {
    public static void main(String[] args) {

        int a=10;

        a+=5;

        System.out.println("a = " + a);

        int b=10;

        b-=5;
        System.out.println("b = " + b);

        int c=10;
        c/=5;
        System.out.println("c = " + c);

        int d=10;
        d*=5;
        System.out.println("d = " + d);
    }
}
